/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    bool isPalindrome(ListNode* head) {
      if (head == nullptr) return true;
      std::vector<int> label;
      ListNode* cur = head;
      while(cur != nullptr) {
        label.emplace_back(cur->val);
        cur = cur->next;
      }
      cur = head;
      for (auto iter = label.rbegin(); iter != label.rend(); ++iter) {
        if (*iter != cur->val) return false;
        cur = cur->next;
      }
      return true;
    }
};
